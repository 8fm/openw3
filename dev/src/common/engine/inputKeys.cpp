String CInputKeys::MakeNameReadable( const String &keyCodeName )
{
	String res = keyCodeName.RightString( keyCodeName.GetLength() - 3 );
	ASSERT( res.GetLength() );

	for ( Uint32 i = 1; i < res.GetLength(); ++i )
	{
		Char &prevChar = res[i-1];
		Char &currChar = res[i];

		if ( currChar == TXT('_') )
			currChar = TXT(' ');
		else
		if ( ( currChar >= TXT('A') && currChar <= TXT('Z') ) &&
			 ( prevChar != TXT(' ') && ( prevChar < TXT('A') && prevChar > TXT('Z') ) ) )
		{
			res = res.LeftString( i ) + TXT(" ") + res.RightString( res.GetLength() - i );
			++i;
		}
	}

	return res;
}

